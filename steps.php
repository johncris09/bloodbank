              <div id="accordion1_2" class="panel-collapse collapse">
                <div class="panel-body"style = "background-color:lightgrey;">
                  <a><strong>EAT</strong></a>
                  <p>You should not donate on an empty stomach. Eating regularly before donating will help keep your blood sugar levels stable.
                    This is important so that you don't feel lightheaded or dizzy after your donation. Having a snack before donation can help 
                    maintain these blood sugar levels. Ensuring that your diet contains foods rich in iron - such as meats and green leafy vegetables
                   - will help to keep your feeling well during and after donation.</p><br>
                    <a><strong>DRINK</strong></a>
                    <p>Almost half of the blood taht you donate is made up of water. The fluids that you lose  during donation 
                      can cause a drop in blood pressure - causing you to feel faint and dizzy. <br> To help prevent this from happening we 
                      ask you to drink 500ml of water immediately before you give blood - we'll give this to you before you donate.
                      It's also important to ensure that you are well hydrated in the days leading up to your donation.
                      This will help <br> to compensate for the fluids lost during donation, and will help to bring your blood volume
                      levels back to normal. it is essential to avoid alcohol before and after donating as this may affect hydration levels
                      and delay recovery.</p><br>
                  <a><strong>EXERCISE</strong></a>
                  <p>Avoid doing any vigorous exercise or heavy lifting the day your donation - both before and after you've given blood.
                    Keeping your body in a rested state is important to give it a chance to replenish the fluids lost during donation
                    , which will help you avoid feeling dizzy or lightheaded and keep you well. light exercise such as walking is fine,
                    but please make sure that you are fully recovered and hydrated prior to your donation.</p><br>
                  <a><strong>WEAR</strong></a>
                  <p>We will need to roll up your sleeves past your elbow when you give blood - to allow easy acces to your veins.
                    To make sure that you are comfortable, please waer clothing with loose sleeves.</p><br>
                  <a><strong>DISTRACT</strong></a>
                  <p>It is normal to feel nervous when you come along to donate - particularly if this is your first time. Our 
                    friendly  and helpful staff will put you at ease straightaway. Distraction is a key. You may want to bring 
                    a friend to wait with you, or feel free to bring a book to read or listen to some music while you donate.
                    We also ask donors to undertake something called "applied muscle tension" or AMT. This is a simple behavioural
                    technique that helps you to maintain blood pressure, and so stops you from feeling faint or unwell. It is 
                    also a brilliant distraction technique! AMT involves tensing and relaxing the body's major muscles during the
                    donation process. A favourite of ours is clenching and unclenching your buttock muscles (no joke!). Further
                    information about this will be available at your donation , or simply ask a member of staff who will be happy
                    to advice you further.</p><br>
                  <a><strong>KNOW</strong></a>
                  <p>Once you've made the decision to donate and made the effort to go - along to your appointment, 
                    you want to be sure that you can actually give blood. Although most people can give blood, there are some
                    restrictions - depending on things like your health, medication, and whether you've been abroad recently.
                    Check who can give give blood using our interactive questions which cover the most common reasons donors 
                    are unable to donate when they attend their appointment.</p><br>
                  <a><strong>SLEEP</strong></a>
                  <p>Where recommended that you have a full night's sleep of beetwen 7 to 9 hours the night before donation.
                    This will help you to feel more alert when you give blood, which will in turn reduce the risk to feeling
                    unwell.</p><br>
