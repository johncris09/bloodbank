              <div id="accordion1_3" class="panel-collapse collapse">
                <div class="panel-body"style = "background-color:lightgrey;">
                  <p><strong>From the moment you arrive at your donation session, we will guide you <br> through the process, explain
                    what we are doing and answer any questions <br> you may have.</strong></p>
                  <a><strong>Welcome And Preperations<strong></a>
                  <p>Before donating blood, please eat regular meals, drink plenty of fluid (non-alcoholic) and avoid vigorous exercise 
                    or exertion. We must give you sufficient information so that you can make an informed choice on the benefits and risks
                    of donating blood and samples for testing. We will provide 500ml of fluid just before you donate. By drinking this
                    over about 5 minutes, it wil help your well being and after donation.</p>
                  <a>Health Screening</a>
                  <p>You will be called private health screening, where a donor career will confirm your identity. It's critical
                    that we guarantee an accurate link between you, your Donor Health Check and your donation. We must ensure
                    that it's safe for you to donate and your donation is safe for a patient to recieve. We'll ask some confidential 
                    questions based on your completed Donor Health Check. There is a period of time from contact with any infection
                    to detecting markers in the laboratory. We trust your honesty when answering these pesonal questions. This will 
                    ensure your blood is safe to transfuse to patients. You may be referred to a registered nurse for certain medical
                    queries: their uniforms have red piping on the nurse collar and cuffs. an explenation will always be provided if you 
                    are not able to donate. You will be advised when you can donate, and we'll encourage you to make an appointment
                    before you leave the team are only able to book future appointments for the venue they are currently. For your 
                    wellbeing, we must ensure you have enough haemoglobin(iron) in your blood before donating. A small blood 
                    sample will be taken from your finger to test your haemoglobin levels. If your test is borderline, we will
                    offer you a confirmatory test. This requires a sample of blood from your arm, to be tested in a machine at the session.
                    We will advice you of the result. Following health screening, we will be asked to sit in a different waiting area.
                    from here you'll be called and invited to a donation chair.</p><br>
                  <a><strong>Your Donation</strong></a>
                  <p>When you are comfortable of the chair, you will be asked your name, address, and date of birth again. Blood bags and
                    sample tubes are labeled with unique donor identification number. All collection equipment is single used and 
                    sterile, only one blood bag is filled with your donation. A blood pressure cuff will be placed on your arm to
                    maintain a small amount of pressure during your donation. The cuff is not used to measure blood pressure. Your 
                    arm will be thoroughly examined to find a suitable vein before being cleaned with a anti-septic sponge. This 
                    skills normal bacteria(Germs) on the skin. You will see staff cleaning their hands frequently, using hand wipes
                    or gills. Donors are adviced to do applied muscle tension exercises during donation. This is to maintain blood
                    pressure and promote wellbeing during and after donation. Following needle insertion you should be comforttable
                    during your donation. If you experience any discomfort or pain please alert a member of staff. An Agitator scale
                    constantly weights and measures your donations it will stop automatically when your donation is comlpete. Donor
                    careers will be available to you through out your donation. You may hear beeping noises from the agitator, this
                    inform as of the progress of your donation. A full donation is 470ml and will usually take between 5 and 10 minutes.
                    If you require assistance or have any concerns, please make as aware by rising your non-donation arm. After Donation,
                    the needle wil be removed a sterile dressing applied to your arm. We advised that you leave the pressure roll on for
                    30 minutes and the plaster dressing on for 6 hours. please avoid using disarm to push on or to carry anything heavy.
                    We will give you a card at the end of your donation with important care advice. Donors are also asked to apply additional
                    pressure to the venepuncture site while waiting order drink at the refreshment area.</p><br>
                  <a><strong>After Donation Refreshment</strong></a>
                  <p>A selection of drinks and snacks are available at the refreshment table. We encourage donors to relax her for atleast 
                    15 minutes and to have atleast 2 drinks following donation. If you feel unwell, please remain seated and alert a member
                    of staff immediately. Where possible, we encourage you to book an appointment for your next donation.</p><br>
                  <a><strong>At Home</strong></a>
                  <p>If you become unwell(other than a cold or coldsore) within 2 weeks following your donation, or if you believe there is a
                    reason your blood should not be given to a patient, <strong>Please Call Our Donor Help Line on(034) 435 8798.</strong> 
                    <strong style = "color:red;">Please Do not Have a Hot Bath on the Day of Your Donation.</strong></p><br>
